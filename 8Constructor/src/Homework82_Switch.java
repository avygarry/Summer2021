
public class Homework82_Switch {
	public static void main(String[] args) {
		boolean switchOn = true;
		boolean switchOff = false;
		boolean lampOn = true;

		if (switchOn = true) {
			switchOff = false;
			lampOn = true;
		} else if (switchOff = true) {
			switchOn = false;
			lampOn = false;
		}

	}

}
