
public class Homework83_Planets {
	
	public long orbitalDistance;
	public long distanceFromSun;
	public long surfaceArea;
	public long lengthOfDay;
	public long radius;
	public boolean hasRings;
	public long getOrbitalDistance() {
		return orbitalDistance;
	}
	public void setOrbitalDistance(long orbitalDistance) {
		this.orbitalDistance = orbitalDistance;
	}
	public long getDistanceFromSun() {
		return distanceFromSun;
	}
	public void setDistanceFromSun(long distanceFromSun) {
		this.distanceFromSun = distanceFromSun;
	}
	public long getSurfaceArea() {
		return surfaceArea;
	}
	public void setSurfaceArea(long surfaceArea) {
		this.surfaceArea = surfaceArea;
	}
	public long getLengthOfDay() {
		return lengthOfDay;
	}
	public void setLengthOfDay(long lengthOfDay) {
		this.lengthOfDay = lengthOfDay;
	}
	public long getRadius() {
		return radius;
	}
	public void setRadius(long radius) {
		this.radius = radius;
	}
	public boolean isHasRings() {
		return hasRings;
	}
	public void setHasRings(boolean hasRings) {
		this.hasRings = hasRings;
	}
	
}