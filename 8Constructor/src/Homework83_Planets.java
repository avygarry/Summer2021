
public class Homework83_Planets {
	
	public int orbitalDistance;
	public int distanceFromSun;
	public int surfaceArea;
	public int lengthOfDay;
	public int radius;
	public boolean hasRings;
	public int getOrbitalDistance() {
		return orbitalDistance;
	}
	public void setOrbitalDistance(int orbitalDistance) {
		this.orbitalDistance = orbitalDistance;
	}
	public int getDistanceFromSun() {
		return distanceFromSun;
	}
	public void setDistanceFromSun(int distanceFromSun) {
		this.distanceFromSun = distanceFromSun;
	}
	public int getSurfaceArea() {
		return surfaceArea;
	}
	public void setSurfaceArea(int surfaceArea) {
		this.surfaceArea = surfaceArea;
	}
	public int getLengthOfDay() {
		return lengthOfDay;
	}
	public void setLengthOfDay(int lengthOfDay) {
		this.lengthOfDay = lengthOfDay;
	}
	public int getRadius() {
		return radius;
	}
	public void setRadius(int radius) {
		this.radius = radius;
	}
	public int getHasRings() {
		return hasRings;
	}
	public void setHasRings(int hasRings) {
		this.hasRings = hasRings;
	}
	
	

}
