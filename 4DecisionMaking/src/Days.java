
public class Days {

	public static void main(String[] args) {
		int num = 24;
		if (num == 1) {
			System.out.println("Sunday");
		}else if (num == 2) {
			System.out.println("Monday");
		}else if (num == 3) {
			System.out.println("Tuesday");
		}else if (num == 4) {
			System.out.println("Wednesday");
		}else {
			System.out.println(num +" isn't a valid number.");
				
		}

	}

}
