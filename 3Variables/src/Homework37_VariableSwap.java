/**
 * Chapter 3 Homework 7
 * This program will swap 2 variables
 * @author Avy
 */
public class Homework37_VariableSwap {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 20;
		int num3 = 0;
		num3 = num1;
		num1 = num2;
		num2 = num3;
		num3 = 0;
		System.out.println(num1);
		System.out.println(num2);
		
	}

}